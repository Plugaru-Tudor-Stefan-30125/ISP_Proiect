    private void groupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_groupButtonActionPerformed
         Map<String, List<Equipment>> groupedList = EController.getEquipmentsGroupedByOwner();
         
         String outputMessage = "Grouped Equipments:\n";
         
         for (Map.Entry mapElement : groupedList.entrySet()) {
            String key = (String)mapElement.getKey();
            
            outputMessage = outputMessage + "\nFrom " + key + ":\n";
            
            List<Equipment> myList = (List)mapElement.getValue();

            for (Equipment elements: myList){
                outputMessage = outputMessage + "   Name:" + elements.getName() + "; Serial Number: "+elements.getSerialNumber() + ";\n";
            }
        }
         
        messageBox.setText(outputMessage);
    }//GEN-LAST:event_groupButtonActionPerformed

    private void removeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeButtonActionPerformed
        String serialNumber = JOptionPane.showInputDialog(this, "Enter Serial Number of the Equipment");
        
        Equipment removedEqipment = EController.removeEquipmentBySerialNumber(serialNumber);
        
        messageBox.setText("Remove Equipment:\n");
        
        if(removedEqipment == null){
            messageBox.setText("The "+serialNumber+" is NOT found");
        }
        else{
            messageBox.setText("The "+serialNumber+" is removed");
        }
    }//GEN-LAST:event_removeButtonActionPerformed

