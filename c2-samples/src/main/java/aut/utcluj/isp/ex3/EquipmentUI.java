    private void getButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getButtonActionPerformed
        List<Equipment> equipmentList = new ArrayList<Equipment>();
        
        equipmentList = EController.getEquipments();
        
        String outputMessage = "Get Equipments:";
        
        for(Equipment e : equipmentList){
            outputMessage = outputMessage + "\n" + "Serial Number: " + e.getSerialNumber() + "; Name:" + e.getName() + "; Owner:" + e.getOwner();
        }
        
        messageBox.setText(outputMessage);
    }//GEN-LAST:event_getButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        String serialNumber = JOptionPane.showInputDialog(this, "Enter Serial Number of the Equipment");
        String nameOfEquipment = JOptionPane.showInputDialog(this, "Enter the Name of the Equipment");
        String nameOfOwner = JOptionPane.showInputDialog(this, "Enter the Owner's Name of the Equipment");
        
        Equipment newEquipment = new Equipment(nameOfEquipment, serialNumber, nameOfOwner);
        
        String outputMessage = "Add Equipment:";
        
        if(serialNumber.equals("")){
            outputMessage = outputMessage + "\n\n" + "Please insert a Serial Number";
        }
        else{
            if(EController.addEquipment(newEquipment)){
                outputMessage = outputMessage + "\n\n" +serialNumber+" is added";
            }
            else{
                outputMessage = outputMessage + "\n\n" + "This Equipment is already added";
            }
        }
        
        messageBox.setText(outputMessage);
    }//GEN-LAST:event_addButtonActionPerformed

